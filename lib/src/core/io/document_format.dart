/// Supported document formats for loading and saving.
enum DocumentFormat {
  /// The human-readable .markdraw format.
  markdraw,

  /// The Excalidraw JSON format (.excalidraw or .json).
  excalidraw,

  /// The .markdrawlib library format.
  markdrawLibrary,

  /// The Excalidraw library format (.excalidrawlib).
  excalidrawLibrary,
}
